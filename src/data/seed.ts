function timedItem(plan: Plan, date: string, title: string, startTime: string, endTime: string, estimatedMinutes: number, sequence: number, notes: string): ScheduleItem {
  return {
    id: `item-${plan.id}-${date}`,
    planId: plan.id,
    title,
    date,
    startTime,
    endTime,
    allDay: false,
    plannedSequence: sequence,
    status: 'scheduled',
    isAutoGenerated: true,
    isRestDayOverride: false,
    estimatedMinutes,
    notes,
    priority: 'normal',
    categoryId: plan.categoryId,
    createdAt: SEED_NOW,
    updatedAt: SEED_NOW,
  }
}

