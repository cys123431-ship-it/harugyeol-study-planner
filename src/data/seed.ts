function basePlan(overrides: Partial<Plan> & Pick<Plan, 'id' | 'title' | 'categoryId' | 'planType' | 'startDate' | 'color'>): Plan {
  return {
    description: '',
    timezone: 'Asia/Seoul',
    recurrenceRule: { frequency: 'daily', interval: 1 },
    excludedWeekdays: [0],
    excludedDates: [],
    carryOverPolicy: 'ask',
    status: 'active',
    createdAt: SEED_NOW,
    updatedAt: SEED_NOW,
    ...overrides,
  }
}

