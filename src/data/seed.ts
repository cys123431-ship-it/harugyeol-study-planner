export function ensureAugustCurriculum(data: PlannerData): { data: PlannerData; added: boolean } {
  const appliedMigrations = data.settings.appliedMigrations ?? []
  if (appliedMigrations.includes(AUGUST_CURRICULUM_MIGRATION_ID)) return { data, added: false }

  const nextSettings = {
    ...data.settings,
    appliedMigrations: [...appliedMigrations, AUGUST_CURRICULUM_MIGRATION_ID],
    lastModifiedAt: new Date().toISOString(),
  }
  if (data.plans.some((plan) => plan.id === AUGUST_PLAN_ID)) {
    return { data: { ...data, settings: nextSettings }, added: false }
  }

  let nextCategories = data.categories
  let category = data.categories.find((candidate) => candidate.id === 'cat-major')
    ?? data.categories.find((candidate) => candidate.type === 'study')
    ?? data.categories[0]
  if (!category) {
    category = { id: 'cat-major', name: '전공', type: 'study', color: '#8096c7', order: 0 }
    nextCategories = [category]
  }

  const template = createAugustTemplate(category.id)
  const stageIds = new Set(template.stages.map((stage) => stage.id))
  return {
    added: true,
    data: {
      ...data,
      categories: nextCategories,
      plans: [...data.plans, template.plan],
      stages: [...data.stages.filter((stage) => stage.planId !== AUGUST_PLAN_ID && !stageIds.has(stage.id)), ...template.stages],
      items: [...data.items.filter((item) => item.planId !== AUGUST_PLAN_ID && !item.id.startsWith('item-august-')), ...template.items],
      settings: nextSettings,
    },
  }
}

export function ensureCertificateStartDate(data: PlannerData): { data: PlannerData; changed: boolean } {
  const appliedMigrations = data.settings.appliedMigrations ?? []
  if (appliedMigrations.includes(CERTIFICATE_START_MIGRATION_ID)) return { data, changed: false }

  const now = new Date().toISOString()
  const nextSettings = {
    ...data.settings,
    appliedMigrations: [...appliedMigrations, CERTIFICATE_START_MIGRATION_ID],
    lastModifiedAt: now,
  }
  const plan = data.plans.find((candidate) => candidate.id === CERTIFICATE_PLAN_ID)
  if (!plan) return { data: { ...data, settings: nextSettings }, changed: false }

  const updatedPlan = { ...plan, startDate: '2026-07-16', updatedAt: now }
  const generated = generateCountSchedule(updatedPlan, data.settings)
  const dateBySequence = new Map(generated.map((item) => [item.plannedSequence, item.date]))
  return {
    changed: true,
    data: {
      ...data,
      plans: data.plans.map((candidate) => candidate.id === CERTIFICATE_PLAN_ID ? updatedPlan : candidate),
      items: data.items.map((item) => {
        if (item.planId !== CERTIFICATE_PLAN_ID || !item.plannedSequence) return item
        const date = dateBySequence.get(item.plannedSequence)
        return date ? { ...item, date, updatedAt: now } : item
      }),
      settings: nextSettings,
    },
  }
}

function keyedPlanItems(items: ScheduleItem[], planId: string) {
  const sorted = [...items].sort((a, b) => a.date.localeCompare(b.date)
    || (a.stageId ?? '').localeCompare(b.stageId ?? '')
    || (a.plannedSequence ?? 0) - (b.plannedSequence ?? 0)
    || a.id.localeCompare(b.id))
  if (planId === MAJOR_PLAN_ID) {
    const stageCounts = new Map<string, number>()
    return sorted.map((item) => {
      const stage = item.stageId ?? 'none'
      const occurrence = (stageCounts.get(stage) ?? 0) + 1
      stageCounts.set(stage, occurrence)
      return { item, key: `${stage}:${occurrence}` }
    })
  }
  if (planId === AUGUST_PLAN_ID) {
    return sorted.map((item) => ({ item, key: `${item.stageId ?? 'none'}:${item.plannedSequence ?? item.date}` }))
  }
  return sorted.map((item) => ({ item, key: String(item.plannedSequence ?? item.date) }))
}

function mergePlanItems(existing: ScheduleItem[], template: ScheduleItem[], planId: string, now: string) {
  const existingByKey = new Map(keyedPlanItems(existing, planId).map(({ item, key }) => [key, item]))
  const usedIds = new Set<string>()
  const aligned = keyedPlanItems(template, planId).map(({ item, key }) => {
    const previous = existingByKey.get(key)
    if (!previous) return item
    usedIds.add(previous.id)
    return {
      ...item,
      id: previous.id,
      title: previous.title,
      startTime: previous.startTime ?? item.startTime,
      endTime: previous.endTime ?? item.endTime,
      allDay: (previous.startTime ?? item.startTime) ? false : item.allDay,
      status: previous.status,
      isAutoGenerated: previous.isAutoGenerated,
      estimatedMinutes: previous.estimatedMinutes,
      actualMinutes: previous.actualMinutes,
      actualSequence: previous.actualSequence,
      completedAt: previous.completedAt,
      notes: previous.notes,
      priority: previous.priority,
      createdAt: previous.createdAt,
      updatedAt: now,
    }
  })
  const extraUserItems = existing.filter((item) => !usedIds.has(item.id) && !item.isAutoGenerated && fromDateKey(item.date).getDay() !== 0)
  return [...aligned, ...extraUserItems]
}

export function ensureSummerRoutine(data: PlannerData): { data: PlannerData; changed: boolean } {
  const appliedMigrations = data.settings.appliedMigrations ?? []
  if (appliedMigrations.includes(SUMMER_ROUTINE_MIGRATION_ID)) return { data, changed: false }

  const now = new Date().toISOString()
  const template = createSampleData()
  const targetPlanIds = new Set([CERTIFICATE_PLAN_ID, MAJOR_PLAN_ID, AUGUST_PLAN_ID, EXERCISE_PLAN_ID, STUDY_CAFE_PLAN_ID])
  const templatePlans = new Map(template.plans.map((plan) => [plan.id, plan]))
  const existingPlanIds = new Set(data.plans.map((plan) => plan.id))
  const plans = [
    ...data.plans.map((plan) => {
      const next = templatePlans.get(plan.id)
      if (!next) return plan
      return {
        ...plan,
        startDate: next.startDate,
        endDate: next.endDate,
        excludedWeekdays: [...new Set([...plan.excludedWeekdays, 0])],
        updatedAt: now,
      }
    }),
    ...template.plans.filter((plan) => !existingPlanIds.has(plan.id)),
  ]
  const existingStageIds = new Set(data.stages.map((stage) => stage.id))
  const stages = [...data.stages, ...template.stages.filter((stage) => !existingStageIds.has(stage.id))]
  const items = [
    ...data.items.filter((item) => !item.planId || !targetPlanIds.has(item.planId)),
    ...template.plans.flatMap((plan) => mergePlanItems(
      data.items.filter((item) => item.planId === plan.id),
      template.items.filter((item) => item.planId === plan.id),
      plan.id,
      now,
    )),
  ]
  const existingCategoryIds = new Set(data.categories.map((category) => category.id))
  const nextSettings = {
    ...data.settings,
    defaultRestWeekdays: [...new Set([...data.settings.defaultRestWeekdays, 0])],
    appliedMigrations: [...appliedMigrations, SUMMER_ROUTINE_MIGRATION_ID],
    lastModifiedAt: now,
  }
  return {
    changed: true,
    data: {
      ...data,
      plans,
      stages,
      items,
      categories: [...data.categories, ...template.categories.filter((category) => !existingCategoryIds.has(category.id))],
      settings: nextSettings,
    },
  }
}
